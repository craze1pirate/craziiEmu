            if (instruction.Op1Register >= Register.AL && instruction.Op1Register <= Register.R15L) srcValue &= 0xFF;
            else if (instruction.Op1Register >= Register.AX && instruction.Op1Register <= Register.R15W) srcValue &= 0xFFFF;
            else if (instruction.Op1Register >= Register.EAX && instruction.Op1Register <= Register.R15D) srcValue &= 0xFFFFFFFF;
        }
        else if (instruction.Op1Kind == OpKind.Memory)
        {
            ulong address = GetMemoryAddress(ref instruction);
            if (instruction.MemorySize == MemorySize.UInt8 || instruction.MemorySize == MemorySize.Int8) srcValue = _memory.GetSpan(address, 1)[0];
            else if (instruction.MemorySize == MemorySize.UInt16 || instruction.MemorySize == MemorySize.Int16) srcValue = BinaryPrimitives.ReadUInt16LittleEndian(_memory.GetSpan(address, 2));
            else if (instruction.MemorySize == MemorySize.UInt32 || instruction.MemorySize == MemorySize.Int32) srcValue = BinaryPrimitives.ReadUInt32LittleEndian(_memory.GetSpan(address, 4));
            else srcValue = BinaryPrimitives.ReadUInt64LittleEndian(_memory.GetSpan(address, 8));
        }
        else throw new InvalidOpcodeException(_context.Rip, 0);

        SetRegister(instruction.Op0Register, srcValue);
    }

