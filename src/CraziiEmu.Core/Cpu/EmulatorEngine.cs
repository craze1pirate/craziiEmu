    private void HandleBextr(ref Instruction instruction)
    {
        ulong srcValue = 0;
        if (instruction.Op1Kind == OpKind.Register) srcValue = GetRegister(instruction.Op1Register);
        else if (instruction.Op1Kind == OpKind.Memory) srcValue = ReadMemory(ref instruction, GetMemoryAddress(ref instruction));

        ulong control = 0;
        if (instruction.Op2Kind == OpKind.Register) control = GetRegister(instruction.Op2Register);
        else if (IsImmediate(instruction.Op2Kind)) control = instruction.GetImmediate(2);
        
        int start = (int)(control & 0xFF);
        int length = (int)((control >> 8) & 0xFF);
        
        int opSize = GetOpSize(ref instruction, 0);
        int maxBits = opSize * 8;
        
        ulong result = 0;
        if (start < maxBits)
        {
            if (length > maxBits - start) length = maxBits - start;
            if (length > 0)
            {
                ulong mask = (length == 64) ? ulong.MaxValue : ((1UL << length) - 1);
                result = (srcValue >> start) & mask;
            }
        }
        
        UpdateBitwiseFlags(result, opSize);
        SetRegister(instruction.Op0Register, result);
    }

    private void UpdateFlags(ulong src0, ulong src1, ulong result, int size)
    {
        UpdateSubFlags(src0, src1, result, size);
    }

