    private void HandleImul(ref Instruction instruction)
    {
        if (instruction.OpCount == 1)
        {
            ulong srcValue = 0;
            if (instruction.Op0Kind == OpKind.Register) srcValue = GetRegister(instruction.Op0Register);
            else if (instruction.Op0Kind == OpKind.Memory) srcValue = ReadMemory(ref instruction, GetMemoryAddress(ref instruction));
            
            long result = (long)_context[CpuRegister.Rax] * (long)srcValue;
            _context[CpuRegister.Rax] = (ulong)result;
            _context[CpuRegister.Rdx] = (result < 0) ? ~(ulong)0 : 0; 
        }
        else if (instruction.OpCount == 2)
        {
            ulong srcValue = 0;
            if (instruction.Op1Kind == OpKind.Register) srcValue = GetRegister(instruction.Op1Register);
            else if (instruction.Op1Kind == OpKind.Memory) srcValue = ReadMemory(ref instruction, GetMemoryAddress(ref instruction));
            else if (IsImmediate(instruction.Op1Kind)) srcValue = instruction.GetImmediate(1);
            
            ulong dstValue = GetRegister(instruction.Op0Register);
            long result = (long)dstValue * (long)srcValue;
            SetRegister(instruction.Op0Register, (ulong)result);
        }
        else if (instruction.OpCount == 3)
        {
            ulong srcValue = 0;
            if (instruction.Op1Kind == OpKind.Register) srcValue = GetRegister(instruction.Op1Register);
            else if (instruction.Op1Kind == OpKind.Memory) srcValue = ReadMemory(ref instruction, GetMemoryAddress(ref instruction));

            ulong imm = instruction.GetImmediate(2);
            long result = (long)srcValue * (long)imm;
            SetRegister(instruction.Op0Register, (ulong)result);
        }
        else throw new InvalidOpcodeException(_context.Rip, 0);
    }

    private void HandleLzcnt(ref Instruction instruction)
    {
        ulong src;
        if (instruction.Op1Kind == OpKind.Register)
        {
            src = GetRegister(instruction.Op1Register);
        }
        else if (instruction.Op1Kind == OpKind.Memory)
        {
            src = ReadMemory(ref instruction, GetMemoryAddress(ref instruction));
        }
        else
        {
            throw new Exception($"Unsupported LZCNT operand 1 kind: {instruction.Op1Kind}");
        }

        int size = instruction.Op0Register.GetSize();
        ulong result;

        if (size == 2)
        {
            result = (ulong)System.Numerics.BitOperations.LeadingZeroCount((uint)(src & 0xFFFF)) - 16;
        }
        else if (size == 4)
        {
            result = (ulong)System.Numerics.BitOperations.LeadingZeroCount((uint)src);
        }
        else if (size == 8)
        {
            result = (ulong)System.Numerics.BitOperations.LeadingZeroCount((ulong)src);
        }
        else
        {
            throw new Exception($"Unsupported LZCNT size: {size}");
        }

        SetRegister(instruction.Op0Register, result);

        // Update flags
        // CF = 1 if SRC == 0, else 0
        // ZF = 1 if result == 0, else 0
        // SF, OF, AF, PF are undefined. We just clear them.
        _context.CarryFlag = (src == 0);
        _context.ZeroFlag = (result == 0);
        _context.SignFlag = false;
        _context.OverflowFlag = false;
    }

    private void HandleMul(ref Instruction instruction)
    {
        int size = instruction.Op0Register.GetSize();
        if (instruction.Op0Kind == OpKind.Memory)
        {
            size = instruction.MemorySize.GetSize();
        }

        ulong operand;
        if (instruction.Op0Kind == OpKind.Register)
        {
            operand = GetRegister(instruction.Op0Register);
        }
        else if (instruction.Op0Kind == OpKind.Memory)
        {
            operand = ReadMemory(ref instruction, GetMemoryAddress(ref instruction));
        }
        else
        {
            throw new Exception($"Unsupported MUL operand kind: {instruction.Op0Kind}");
        }

        if (size == 1) // AL * r/m8
        {
            ushort result = (ushort)((GetRegister(Iced.Intel.Register.AL) & 0xFF) * (operand & 0xFF));
            SetRegister(Iced.Intel.Register.AX, result);
        }
        else if (size == 2) // AX * r/m16 -> DX:AX
        {
            uint result = (uint)((GetRegister(Iced.Intel.Register.AX) & 0xFFFF) * (operand & 0xFFFF));
            SetRegister(Iced.Intel.Register.AX, result & 0xFFFF);
            SetRegister(Iced.Intel.Register.DX, result >> 16);
        }
        else if (size == 4) // EAX * r/m32 -> EDX:EAX
        {
            ulong result = (ulong)(GetRegister(Iced.Intel.Register.EAX) & 0xFFFFFFFF) * (ulong)(operand & 0xFFFFFFFF);
            SetRegister(Iced.Intel.Register.RAX, result & 0xFFFFFFFF); // zero extends to RAX
            SetRegister(Iced.Intel.Register.RDX, result >> 32);        // zero extends to RDX
        }
        else if (size == 8) // RAX * r/m64 -> RDX:RAX
        {
            ulong rax = GetRegister(Iced.Intel.Register.RAX);
            UInt128 result = (UInt128)rax * operand;
            SetRegister(Iced.Intel.Register.RAX, (ulong)result);
            SetRegister(Iced.Intel.Register.RDX, (ulong)(result >> 64));
        }
        else
        {
            throw new Exception($"Unsupported MUL size: {size}");
        }
    }

    private void HandleDiv(ref Instruction instruction)
    {
        ulong divisor;
        if (instruction.Op0Kind == OpKind.Register)
            divisor = GetRegister(instruction.Op0Register);
        else if (instruction.Op0Kind == OpKind.Memory)
            divisor = ReadMemory(ref instruction, GetMemoryAddress(ref instruction));
        else
            throw new InvalidOpcodeException(_context.Rip, 0);

        if (divisor == 0)
            throw new DivideByZeroException($"Division by zero at RIP 0x{_context.Rip:X16}");

        int size = instruction.Op0Kind == OpKind.Register 
            ? instruction.Op0Register.GetSize() 
            : GetOpSize(ref instruction, 0);

        if (size == 4)
        {
            ulong dividend = ((ulong)(uint)GetRegister(Iced.Intel.Register.EDX) << 32) | (uint)GetRegister(Iced.Intel.Register.EAX);
            uint div = (uint)divisor;
            uint quotient = (uint)(dividend / div);
            uint remainder = (uint)(dividend % div);
            SetRegister(Iced.Intel.Register.RAX, quotient); // SetRegister to RAX zero-extends the 32-bit value in the emulation context
            SetRegister(Iced.Intel.Register.RDX, remainder);
        }
        else if (size == 8)
        {
            UInt128 dividend = new UInt128(GetRegister(Iced.Intel.Register.RDX), GetRegister(Iced.Intel.Register.RAX));
            ulong div = divisor;
            ulong quotient = (ulong)(dividend / div);
            ulong remainder = (ulong)(dividend % div);
            SetRegister(Iced.Intel.Register.RAX, quotient);
            SetRegister(Iced.Intel.Register.RDX, remainder);
        }
        else if (size == 2)
        {
            uint dividend = ((uint)(ushort)GetRegister(Iced.Intel.Register.DX) << 16) | (ushort)GetRegister(Iced.Intel.Register.AX);
            ushort div = (ushort)divisor;
            ushort quotient = (ushort)(dividend / div);
            ushort remainder = (ushort)(dividend % div);
            SetRegister(Iced.Intel.Register.AX, quotient);
            SetRegister(Iced.Intel.Register.DX, remainder);
        }
        else if (size == 1)
        {
            ushort dividend = (ushort)GetRegister(Iced.Intel.Register.AX);
            byte div = (byte)divisor;
            byte quotient = (byte)(dividend / div);
            byte remainder = (byte)(dividend % div);
            SetRegister(Iced.Intel.Register.AL, quotient);
            SetRegister(Iced.Intel.Register.AH, remainder);
        }
        else
        {
            throw new InvalidOpcodeException(_context.Rip, 0);
        }
    }

