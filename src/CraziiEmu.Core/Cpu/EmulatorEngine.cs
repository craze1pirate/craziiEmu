    private void HandleInt3(ref Instruction instruction)
    {
        ulong rip = _context.Rip;
        
        string? symbolName = null;
        if (SelfImportStubs != null && SelfImportStubs.TryGetValue(rip, out var nid))
        {
            symbolName = nid;
        }
        else if (DynamicLinker != null)
        {
            DynamicLinker.TryGetSymbolNameByAddress(rip, out symbolName);
        }

        if (symbolName != null && ModuleManager != null)
        {
            try
            {
                if (ModuleManager.TryDispatch(symbolName, _context, out _))
                {
                    // Simulate x86-64 RET
                    ulong rsp = GetRegister(Iced.Intel.Register.RSP);
                    ulong returnAddress = System.Buffers.Binary.BinaryPrimitives.ReadUInt64LittleEndian(_memory.GetSpan(rsp, 8));
                    _context.Rip = returnAddress;
                    SetRegister(Iced.Intel.Register.RSP, rsp + 8);
                    return;
                }
            }
            catch (NotImplementedException ex)
            {
                throw new Exception($"HLE Method for '{symbolName}' is not implemented.", ex);
            }
        }
        
        throw new Exception($"Unmapped or unhandled HLE stub (INT 3 breakpoint) hit at RIP 0x{rip:X16}. Symbol: {symbolName ?? "Unknown"}");
    }

    private ulong TranslateAddress(ulong address)
    {
        if (address == 0) return _memory.AllocateGpu(0); // Legacy catch for null writes
        
        // Crazii_demo.elf and other simple homebrew assume the GPU framebuffer is hardcoded at 0x200000000.
        // We translate any access in this 256MB range to our dynamic GPU allocation pool.
        if (address >= 0x200000000 && address < 0x210000000)
        {
            ulong baseGpu = _memory.AllocateGpu(0); 
            ulong offset = address - 0x200000000;
            return baseGpu + offset;
        }

        return address;
    }

    private ulong ReadMemory(ref Instruction instruction, ulong address)
    {
        address = TranslateAddress(address);
        
        switch (instruction.MemorySize)
        {
            case MemorySize.UInt8:
            case MemorySize.Int8:
                return _memory.GetSpan(address, 1)[0];
            case MemorySize.UInt16:
            case MemorySize.Int16:
                return BinaryPrimitives.ReadUInt16LittleEndian(_memory.GetSpan(address, 2));
            case MemorySize.UInt32:
            case MemorySize.Int32:
                return BinaryPrimitives.ReadUInt32LittleEndian(_memory.GetSpan(address, 4));
            case MemorySize.UInt64:
            case MemorySize.Int64:
            default:
                // Fallback to register size if MemorySize is Unknown
                if (instruction.Op0Kind == OpKind.Register && instruction.Op0Register.GetSize() == 4)
                    return BinaryPrimitives.ReadUInt32LittleEndian(_memory.GetSpan(address, 4));
                if (instruction.Op1Kind == OpKind.Register && instruction.Op1Register.GetSize() == 4)
                    return BinaryPrimitives.ReadUInt32LittleEndian(_memory.GetSpan(address, 4));
                return BinaryPrimitives.ReadUInt64LittleEndian(_memory.GetSpan(address, 8));
        }
    }

    private void WriteMemory(ref Instruction instruction, ulong address, ulong value)
    {
        address = TranslateAddress(address);

        switch (instruction.MemorySize)
        {
            case MemorySize.UInt8:
            case MemorySize.Int8:
                _memory.GetSpan(address, 1)[0] = (byte)value;
                break;
            case MemorySize.UInt16:
            case MemorySize.Int16:
                BinaryPrimitives.WriteUInt16LittleEndian(_memory.GetSpan(address, 2), (ushort)value);
                break;
            case MemorySize.UInt32:
            case MemorySize.Int32:
                BinaryPrimitives.WriteUInt32LittleEndian(_memory.GetSpan(address, 4), (uint)value);
                break;
            case MemorySize.UInt64:
            case MemorySize.Int64:
            default:
                if (instruction.Op0Kind == OpKind.Register && instruction.Op0Register.GetSize() == 4)
                    BinaryPrimitives.WriteUInt32LittleEndian(_memory.GetSpan(address, 4), (uint)value);
                else if (instruction.Op1Kind == OpKind.Register && instruction.Op1Register.GetSize() == 4)
                    BinaryPrimitives.WriteUInt32LittleEndian(_memory.GetSpan(address, 4), (uint)value);
                else
                    BinaryPrimitives.WriteUInt64LittleEndian(_memory.GetSpan(address, 8), value);
                break;
        }
    }

    private ulong GetMemoryAddress(ref Instruction instruction)
    {
        if (instruction.IsIPRelativeMemoryOperand)
        {
            return instruction.IPRelativeMemoryAddress;
        }

        ulong address = 0;
        if (instruction.MemorySegment == Register.FS)
        {
            address += _context.FsBase;
            Console.WriteLine($"[FS DEBUG] Instruction: {instruction}, FsBase: {_context.FsBase:X16}, address so far: {address:X16}");
        }
        else if (instruction.MemorySegment == Register.GS)
        {
            address += _context.GsBase;
        }

        if (instruction.MemoryBase != Register.None)
        {
            if (instruction.MemoryBase == Register.RIP || instruction.MemoryBase == Register.EIP)
            {
                address += _context.Rip;
            }
            else
            {
                address += GetRegister(instruction.MemoryBase);
            }
        }
        if (instruction.MemoryIndex != Register.None)
        {
            address += GetRegister(instruction.MemoryIndex) * (ulong)instruction.MemoryIndexScale;
        }
        address += instruction.MemoryDisplacement64;
        return address;
    }

    private ulong GetRegister(Register reg)
    {
        ulong val = MapRegister(reg) switch
        {
            CpuRegister.Rax => _context[CpuRegister.Rax],
            CpuRegister.Rbx => _context[CpuRegister.Rbx],
            CpuRegister.Rcx => _context[CpuRegister.Rcx],
            CpuRegister.Rdx => _context[CpuRegister.Rdx],
            CpuRegister.Rsi => _context[CpuRegister.Rsi],
            CpuRegister.Rdi => _context[CpuRegister.Rdi],
            CpuRegister.Rbp => _context[CpuRegister.Rbp],
            CpuRegister.Rsp => _context[CpuRegister.Rsp],
            CpuRegister.R8 => _context[CpuRegister.R8],
            CpuRegister.R9 => _context[CpuRegister.R9],
            CpuRegister.R10 => _context[CpuRegister.R10],
            CpuRegister.R11 => _context[CpuRegister.R11],
            CpuRegister.R12 => _context[CpuRegister.R12],
            CpuRegister.R13 => _context[CpuRegister.R13],
            CpuRegister.R14 => _context[CpuRegister.R14],
            CpuRegister.R15 => _context[CpuRegister.R15],
            _ => throw new NotImplementedException($"Register {reg} not implemented in Context.")
        };

        if (reg == Register.AH || reg == Register.BH || reg == Register.CH || reg == Register.DH)
            return (val >> 8) & 0xFF;

        int size = reg.GetSize();
        if (size == 1) return val & 0xFF;
        if (size == 2) return val & 0xFFFF;
        if (size == 4) return val & 0xFFFFFFFF;
        return val;
    }

    private void SetRegister(Register reg, ulong value)
    {
        var mapped = MapRegister(reg);
        
        ulong currentValue = mapped switch {
            CpuRegister.Rax => _context[CpuRegister.Rax],
            CpuRegister.Rbx => _context[CpuRegister.Rbx],
            CpuRegister.Rcx => _context[CpuRegister.Rcx],
            CpuRegister.Rdx => _context[CpuRegister.Rdx],
            CpuRegister.Rsi => _context[CpuRegister.Rsi],
            CpuRegister.Rdi => _context[CpuRegister.Rdi],
            CpuRegister.Rbp => _context[CpuRegister.Rbp],
            CpuRegister.Rsp => _context[CpuRegister.Rsp],
            CpuRegister.R8 => _context[CpuRegister.R8],
            CpuRegister.R9 => _context[CpuRegister.R9],
            CpuRegister.R10 => _context[CpuRegister.R10],
            CpuRegister.R11 => _context[CpuRegister.R11],
            CpuRegister.R12 => _context[CpuRegister.R12],
            CpuRegister.R13 => _context[CpuRegister.R13],
            CpuRegister.R14 => _context[CpuRegister.R14],
            CpuRegister.R15 => _context[CpuRegister.R15],
            _ => 0
        };

        ulong newValue;
        if (reg == Register.AH || reg == Register.BH || reg == Register.CH || reg == Register.DH)
        {
            newValue = (currentValue & 0xFFFFFFFFFFFF00FF) | ((value & 0xFF) << 8);
        }
        else
        {
            int size = reg.GetSize();
            if (size == 1) newValue = (currentValue & 0xFFFFFFFFFFFFFF00) | (value & 0xFF);
            else if (size == 2) newValue = (currentValue & 0xFFFFFFFFFFFF0000) | (value & 0xFFFF);
            else if (size == 4) newValue = value & 0xFFFFFFFF; // 32-bit ZERO-EXTENDS to 64-bit!
            else newValue = value;
        }

        switch (mapped)
        {
            case CpuRegister.Rax: _context[CpuRegister.Rax] = newValue; break;
            case CpuRegister.Rbx: _context[CpuRegister.Rbx] = newValue; break;
            case CpuRegister.Rcx: _context[CpuRegister.Rcx] = newValue; break;
            case CpuRegister.Rdx: _context[CpuRegister.Rdx] = newValue; break;
            case CpuRegister.Rsi: _context[CpuRegister.Rsi] = newValue; break;
            case CpuRegister.Rdi: _context[CpuRegister.Rdi] = newValue; break;
            case CpuRegister.Rbp: _context[CpuRegister.Rbp] = newValue; break;
            case CpuRegister.Rsp: _context[CpuRegister.Rsp] = newValue; break;
            case CpuRegister.R8: _context[CpuRegister.R8] = newValue; break;
            case CpuRegister.R9: _context[CpuRegister.R9] = newValue; break;
            case CpuRegister.R10: _context[CpuRegister.R10] = newValue; break;
            case CpuRegister.R11: _context[CpuRegister.R11] = newValue; break;
            case CpuRegister.R12: _context[CpuRegister.R12] = newValue; break;
            case CpuRegister.R13: _context[CpuRegister.R13] = newValue; break;
            case CpuRegister.R14: _context[CpuRegister.R14] = newValue; break;
            case CpuRegister.R15: _context[CpuRegister.R15] = newValue; break;
        }
    }

    private CpuRegister MapRegister(Register reg)
    {
        switch (reg)
        {
            case Register.RAX: case Register.EAX: case Register.AX: case Register.AL: case Register.AH: return CpuRegister.Rax;
            case Register.RBX: case Register.EBX: case Register.BX: case Register.BL: case Register.BH: return CpuRegister.Rbx;
            case Register.RCX: case Register.ECX: case Register.CX: case Register.CL: case Register.CH: return CpuRegister.Rcx;
            case Register.RDX: case Register.EDX: case Register.DX: case Register.DL: case Register.DH: return CpuRegister.Rdx;
            case Register.RSI: case Register.ESI: case Register.SI: case Register.SIL: return CpuRegister.Rsi;
            case Register.RDI: case Register.EDI: case Register.DI: case Register.DIL: return CpuRegister.Rdi;
            case Register.RBP: case Register.EBP: case Register.BP: case Register.BPL: return CpuRegister.Rbp;
            case Register.RSP: case Register.ESP: case Register.SP: case Register.SPL: return CpuRegister.Rsp;
            case Register.R8:  case Register.R8D:  case Register.R8W:  case Register.R8L:  return CpuRegister.R8;
            case Register.R9:  case Register.R9D:  case Register.R9W:  case Register.R9L:  return CpuRegister.R9;
            case Register.R10: case Register.R10D: case Register.R10W: case Register.R10L: return CpuRegister.R10;
            case Register.R11: case Register.R11D: case Register.R11W: case Register.R11L: return CpuRegister.R11;
            case Register.R12: case Register.R12D: case Register.R12W: case Register.R12L: return CpuRegister.R12;
            case Register.R13: case Register.R13D: case Register.R13W: case Register.R13L: return CpuRegister.R13;
            case Register.R14: case Register.R14D: case Register.R14W: case Register.R14L: return CpuRegister.R14;
            case Register.R15: case Register.R15D: case Register.R15W: case Register.R15L: return CpuRegister.R15;
            default: throw new NotImplementedException($"Cannot map Iced register {reg} to CpuRegister.");
        }
    }

    /// <summary>
    /// Starts execution from the provided entry point until termination.
    /// </summary>
    /// <param name="entryPoint">The virtual address to begin execution.</param>
    public void Run(ulong entryPoint)
    {
        _context.Rip = entryPoint;
        _context.IsTerminated = false;
        
        Instruction[] traceBuffer = new Instruction[10];
        int traceIndex = 0;
        long instructionCount = 0;

        while (!_context.IsTerminated)
        {
            ulong ripBefore = _context.Rip;
            Instruction currentInstr = default;
            try
            {
                instructionCount++;
                if (instructionCount > 100000000)
                {
                    throw new Exception("Instruction limit reached (infinite loop suspected)");
                }

                var decoder = Decoder.Create(64, new MemoryCodeReader(_memory, ripBefore), ripBefore, DecoderOptions.None);
                decoder.Decode(out currentInstr);
                
                traceBuffer[traceIndex % 10] = currentInstr;
                traceIndex++;
                Step();
            }
            catch (Exception ex)
            {
                var formatter = new NasmFormatter();
                var output = new StringOutput();
                formatter.Format(currentInstr, output);
                
                var sb = new System.Text.StringBuilder();
                sb.AppendLine($"Instruction execution failed: {output.ToString()} at RIP 0x{ripBefore:X}");
                sb.AppendLine($"RAX: 0x{_context[CpuRegister.Rax]:X16} RBX: 0x{_context[CpuRegister.Rbx]:X16} RCX: 0x{_context[CpuRegister.Rcx]:X16}");
                sb.AppendLine("Recent trace:");
                for (int i = 0; i < 10; i++)
                {
                    int idx = (traceIndex - 10 + i);
                    if (idx >= 0)
                    {
                        var tOut = new StringOutput();
                        formatter.Format(traceBuffer[idx % 10], tOut);
                        sb.AppendLine($"  {tOut.ToString()}");
                    }
                }
                sb.AppendLine($"-> {ex.Message}");
                
                throw new Exception(sb.ToString(), ex);
            }
        }
    }

    private bool IsImmediate(OpKind kind)
    {
        return kind == OpKind.Immediate8 || 
               kind == OpKind.Immediate8_2nd ||
               kind == OpKind.Immediate16 ||
               kind == OpKind.Immediate32 ||
               kind == OpKind.Immediate64 ||
               kind == OpKind.Immediate8to16 ||
               kind == OpKind.Immediate8to32 ||
               kind == OpKind.Immediate8to64 ||
               kind == OpKind.Immediate32to64;
    }
