    /// <summary>
    /// Handles a Push instruction by decrementing RSP and writing the source value to the stack.
    /// </summary>
    private void HandlePush(ref Instruction instruction)
    {
        ulong value;

        if (instruction.Op0Kind == OpKind.Register)
        {
            value = GetRegister(instruction.Op0Register);
        }
        else if (IsImmediate(instruction.Op0Kind))
        {
            value = instruction.GetImmediate(0);
        }
        else
        {
            throw new InvalidOpcodeException(_context.Rip, 0);
        }

        // x86-64 push: RSP -= 8, then write value at [RSP]
        const int StackSlotSize = 8;
        ulong rsp = GetRegister(Iced.Intel.Register.RSP) - StackSlotSize;
        SetRegister(Iced.Intel.Register.RSP, rsp);

        var span = _memory.GetSpan(rsp, StackSlotSize);
        BinaryPrimitives.WriteUInt64LittleEndian(span, value);
    }

    /// <summary>
    /// Handles a Pop instruction by reading the value at [RSP] into the destination register
    /// and incrementing RSP.
    /// </summary>
    private void HandlePop(ref Instruction instruction)
    {
        if (instruction.Op0Kind != OpKind.Register)
        {
            throw new InvalidOpcodeException(_context.Rip, 0);
        }

        const int StackSlotSize = 8;
        ulong rsp = GetRegister(Iced.Intel.Register.RSP);

        var span = _memory.GetSpan(rsp, StackSlotSize);
        ulong value = BinaryPrimitives.ReadUInt64LittleEndian(span);

        SetRegister(instruction.Op0Register, value);
        SetRegister(Iced.Intel.Register.RSP, rsp + StackSlotSize);
    }

    /// <summary>
    /// Handles a Cmp instruction by performing a virtual subtraction and updating CPU flags
    /// without storing the result.
    /// </summary>
    private void HandleCmp(ref Instruction instruction)
    {
        ulong src0;
        ulong src1;

        // Resolve operand 0 (left-hand side)
        if (instruction.Op0Kind == OpKind.Register)
        {
            src0 = GetRegister(instruction.Op0Register);
        }
        else if (instruction.Op0Kind == OpKind.Memory)
        {
            ulong src0Addr = GetMemoryAddress(ref instruction);
            src0 = ReadMemory(ref instruction, src0Addr);
        }
        else
        {
            throw new InvalidOpcodeException(_context.Rip, 0);
        }

        // Resolve operand 1 (right-hand side)
        if (instruction.Op1Kind == OpKind.Register)
        {
            src1 = GetRegister(instruction.Op1Register);
        }
        else if (IsImmediate(instruction.Op1Kind))
        {
            src1 = instruction.GetImmediate(1);
        }
        else if (instruction.Op1Kind == OpKind.Memory)
        {
            ulong src1Addr = GetMemoryAddress(ref instruction);
            src1 = ReadMemory(ref instruction, src1Addr);
        }
        else
        {
            throw new InvalidOpcodeException(_context.Rip, 0);
        }

        ulong result = src0 - src1;
        UpdateFlags(src0, src1, result, GetOpSize(ref instruction, 0));
    }

    /// <summary>
    /// Updates ZeroFlag, SignFlag, and OverflowFlag based on the result of a subtraction (src0 - src1).
    /// Overflow is detected using the standard signed-overflow rule for subtraction.
    /// </summary>

    private void HandleShlx(ref Instruction instruction)
    {
        ulong srcValue = 0;
        if (instruction.Op1Kind == OpKind.Register) srcValue = GetRegister(instruction.Op1Register);
        else if (instruction.Op1Kind == OpKind.Memory) srcValue = ReadMemory(ref instruction, GetMemoryAddress(ref instruction));

        ulong countValue = 0;
        if (instruction.Op2Kind == OpKind.Register) countValue = GetRegister(instruction.Op2Register);

        int size = GetOpSize(ref instruction, 0);
        int shiftMask = size == 8 ? 0x3F : 0x1F;
        int shiftCount = (int)(countValue & (uint)shiftMask);
        
        ulong result = 0;
        if (size == 4)
        {
            result = (uint)srcValue << shiftCount;
        }
        else if (size == 8)
        {
            result = srcValue << shiftCount;
        }
        else
        {
            throw new InvalidOpcodeException(_context.Rip, 0);
        }

        SetRegister(instruction.Op0Register, result);
    }

    private void HandleShrx(ref Instruction instruction)
