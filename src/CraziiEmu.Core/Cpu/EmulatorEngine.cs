    private void HandleRet(ref Instruction instruction)
    {
        const int StackSlotSize = 8;
        ulong rsp = GetRegister(Iced.Intel.Register.RSP);
        var span = _memory.GetSpan(rsp, StackSlotSize);
        _context.Rip = BinaryPrimitives.ReadUInt64LittleEndian(span);
        SetRegister(Iced.Intel.Register.RSP, rsp + StackSlotSize);
    }

    private void HandleCall(ref Instruction instruction)
    {
        ulong nextRip = _context.Rip + (ulong)instruction.Length;
        const int StackSlotSize = 8;
        ulong rsp = GetRegister(Iced.Intel.Register.RSP) - StackSlotSize;
        SetRegister(Iced.Intel.Register.RSP, rsp);
        var span = _memory.GetSpan(rsp, StackSlotSize);
        BinaryPrimitives.WriteUInt64LittleEndian(span, nextRip);
        
        if (instruction.Op0Kind == OpKind.NearBranch64) _context.Rip = instruction.NearBranchTarget;
        else if (instruction.Op0Kind == OpKind.Register) _context.Rip = GetRegister(instruction.Op0Register);
        else if (instruction.Op0Kind == OpKind.Memory) _context.Rip = BinaryPrimitives.ReadUInt64LittleEndian(_memory.GetSpan(GetMemoryAddress(ref instruction), 8));
        else throw new InvalidOpcodeException(_context.Rip, 0);
    }

