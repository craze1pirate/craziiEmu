        else if (instruction.Op1Kind == OpKind.Memory) srcValue = BinaryPrimitives.ReadUInt64LittleEndian(_memory.GetSpan(GetMemoryAddress(ref instruction), 8));
        else throw new InvalidOpcodeException(_context.Rip, 0);

        ulong dstValue = 0;
        ulong address = 0;
        if (instruction.Op0Kind == OpKind.Register) dstValue = GetRegister(instruction.Op0Register);
        else if (instruction.Op0Kind == OpKind.Memory)
        {
            address = GetMemoryAddress(ref instruction);
            if (address == 0 || address == 0x200000000) address = _memory.AllocateGpu(0);
            dstValue = BinaryPrimitives.ReadUInt64LittleEndian(_memory.GetSpan(address, 8));
        }
        else throw new InvalidOpcodeException(_context.Rip, 0);

        ulong result = dstValue & srcValue;
        UpdateBitwiseFlags(result, GetOpSize(ref instruction, 0));

        if (instruction.Op0Kind == OpKind.Register) SetRegister(instruction.Op0Register, result);
        else if (instruction.Op0Kind == OpKind.Memory) BinaryPrimitives.WriteUInt64LittleEndian(_memory.GetSpan(address, 8), result);
    }

